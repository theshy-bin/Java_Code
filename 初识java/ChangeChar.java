public class ChangeChar{
//转义字符
	public static void main(String[] args){

		System.out.println("姓名\t性别\t年龄");
		//\t 一个制表位

		System.out.println("如果冬天来了，\n春天还会远吗？");
		//\? 一个换行符

		System.out.println("\\");
		//\\ 一个\

		System.out.println("\"");
		//\" 一个"

		System.out.println("\'");
		//\' 一个'

		System.out.println("夏春秋冬\r春夏");
		//\r 一个回车符，光标移动到当先行的最左侧



	}
}